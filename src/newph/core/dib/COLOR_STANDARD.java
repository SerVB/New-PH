package newph.core.dib;

/**
 * Standard color constants. (R5G6B5 = RGB16)
 * @author SerVB
 * @since "GitHub new sources"
 */
public class COLOR_STANDARD {
    
    /**
     * White Color. RGB24(255, 255, 255)
     */
    public static final int cColor_Whiteb   = 0b1111_1111_1111_1111;
    
    /**
     * Black Color. RGB24(0, 0, 0)
     */
    public static final int cColor_Black    = 0b0000_0000_0000_0000;
    
    /**
     * Gray Color. RGB24(64, 64, 64)
     */
    public static final int cColor_Gray64   = 0b0100_0010_0000_1000;
    
    /**
     * Gray Color. RGB24(128, 128, 128)
     */
    public static final int cColor_Gray128  = 0b1000_0100_0001_0000;
    
    /**
     * Gray Color. RGB24(192, 192, 192)
     */
    public static final int cColor_Gray192  = 0b1100_0110_0001_1000;
    
    /**
     * Red Color. RGB24(255, 0, 0)
     */
    public static final int cColor_Red      = 0b1111_1000_0000_0000;
    
    /**
     * Green Color. RGB24(0, 64, 0)
     */
    public static final int cColor_Green64  = 0b0000_0010_0000_0000;
    
    /**
     * Green Color. RGB24(0, 128, 0)
     */
    public static final int cColor_Green128 = 0b0000_0011_1110_0000;
    
    /**
     * Green Color. RGB24(0, 192, 0)
     */
    public static final int cColor_Green192 = 0b0000_0101_1110_0000;
    
    /**
     * Green Color. RGB24(0, 255, 0)
     */
    public static final int cColor_Green    = 0b0000_0111_1110_0000;
    
    /**
     * Blue Color. RGB24(0, 0, 64)
     */
    public static final int cColor_Blue64   = 0b0000_0000_0000_1000;
    
    /**
     * Blue Color. RGB24(0, 0, 128)
     */
    public static final int cColor_Blue128  = 0b0000_0000_0001_0000;
    
    /**
     * Blue Color. RGB24(0, 0, 192)
     */
    public static final int cColor_Blue192  = 0b0000_0000_0001_1000;
    
    /**
     * Blue Color. RGB24(0, 0, 255)
     */
    public static final int cColor_Blue     = 0b0000_0000_0001_1111;
    
    /**
     * Yellow Color. RGB24(192, 192, 0)
     */
    public static final int cColor_Yello192 = 0b1011_1101_1110_0000;
    
    /**
     * Yellow Color. RGB24(255, 255, 0)
     */
    public static final int cColor_Yello    = 0b1111_1111_1110_0000;

}
